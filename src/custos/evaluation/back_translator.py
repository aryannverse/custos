import instructor
from groq import Groq
from sentence_transformers import SentenceTransformer, util
from pydantic import BaseModel, Field

from custos.core.config import settings

class BackTranslationResult(BaseModel):
    inferred_question: str = Field(description="The English question that this SQL query answers.")

class BackTranslator:
    """
    Addresses Hallucinations by taking generated SQL and translating it
    BACK into an English question. We then compare the embedding similarity
    of the inferred question with the original user question.
    """
    def __init__(self, api_key: str | None = None, model: str | None = None):
        self.api_key = api_key or settings.groq_api_key
        
        self.model = model or settings.models.secondary
        self.client = instructor.from_groq(Groq(api_key=self.api_key), mode=instructor.Mode.JSON)
        
        
        
        self.embedder = SentenceTransformer(settings.schema.embedding_model)

    def translate_and_compare(self, original_question: str, generated_sql: str) -> float:
        """
        Translates SQL back to English and computes similarity with the original question.
        
        Args:
            original_question: The user's input question.
            generated_sql: The SQL query generated by Phase 2.
            
        Returns:
            A score from 0.0 to 100.0 representing alignment.
        """
        system_prompt = (
            "You are an expert SQL analyst. I will give you a SQL query. "
            "Translate it back into a natural language question. "
            "Be precise about aggregations, filters, and what is being retrieved. "
            "Do not explain the SQL, just provide the question it answers."
        )
        
        try:
            result = self.client.chat.completions.create(
                model=self.model,
                response_model=BackTranslationResult,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": f"SQL Query: {generated_sql}"},
                ],
                temperature=0.0
            )
        except Exception as e:
            import structlog
            logger = structlog.get_logger(__name__)
            logger.error("Back translation failed", error=str(e))
            return 0.0
        inferred = result.inferred_question
        
        
        emb_original = self.embedder.encode(original_question, convert_to_tensor=True)
        emb_inferred = self.embedder.encode(inferred, convert_to_tensor=True)
        
        sim = util.cos_sim(emb_original, emb_inferred).item()
        
        
        return max(0.0, min(1.0, float(sim))) * 100.0
